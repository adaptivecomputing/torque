#include <pbs_config.h>   /* the master config generated by configure */

#include <string.h>
#include <stdio.h>
#include "libpbs.h"
#include "dis.h"
#include "server_limits.h"

int pbs_gpumode_err(
    
  int   c,
  char *node,
  char *gpuid,
  int   gpumode,
  int  *local_errno)

  {
  int                 rc;

  struct batch_reply *reply;

  if ((node == (char *)0) || (gpumode < 0 || gpumode > 3) || (gpuid == (char *)0))
    return (PBSE_IVALREQ);
  
  if ((c < 0) || 
      (c >= PBS_NET_MAX_CONNECTIONS))
    {
    return(PBSE_IVALREQ);
    }

  /* send request */

  if ((rc = PBSD_gpu_put(c, node, gpuid, gpumode, -1, -1, NULL)) != 0)
    return (rc);

  /* read reply */

  reply = PBSD_rdrpy(local_errno, c);

  PBSD_FreeReply(reply);

  return connection[c].ch_errno;
  } /* END pbs_gpumode_err() */





int pbs_gpumode(
    
  int   c,
  char *node,
  char *gpuid,
  int   gpumode)

  {
  pbs_errno = 0;

  return(pbs_gpumode_err(c, node, gpuid, gpumode, &pbs_errno));
  } /* END pbs_gpumode() */




int pbs_gpureset_err(
    
  int   c,
  char *node,
  char *gpuid,
  int   permanent,
  int   vol,
  int  *local_errno)

  {
  int                 rc;

  struct batch_reply *reply;

  if ((node == (char *)0) || ((permanent != 1) && (permanent != 0)) ||
      ((vol != 1) && (vol != 0)) || (gpuid == (char *)0) ||
      ((permanent == 1) && (vol == 1)))
    return (PBSE_IVALREQ);

  /* send request */

  if ((rc = PBSD_gpu_put(c, node, gpuid, -1, permanent, vol, NULL)) != 0)
    return (rc);

  /* read reply */

  reply = PBSD_rdrpy(local_errno, c);

  PBSD_FreeReply(reply);

  return connection[c].ch_errno;
  } /* END pbs_gpureset_err() */




int pbs_gpureset(
    
  int   c,
  char *node,
  char *gpuid,
  int   permanent,
  int   vol)

  {
  pbs_errno = 0;

  return(pbs_gpureset_err(c, node, gpuid, permanent, vol, &pbs_errno));
  } /* END pbs_gpureset() */




