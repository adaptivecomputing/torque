void
dep_initialize(void)
  {
  char *id = "dep_initialize";

  if (kd == NULL)
    {
    if ((kd = kvm_open(NULL, NULL, NULL, O_RDONLY, "rm")) == NULL)
      {
      log_err(errno, id, "kvm_open");
      return;
      }
    }

  if (kvm_nlist(kd, nl) == -1)
    {
    log_err(errno, id, "kvm_nlist");
    return;
    }

  return;
  }

