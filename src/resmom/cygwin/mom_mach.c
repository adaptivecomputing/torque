char *sessions(

  struct rm_attribute *attrib) /* I */

  {
  char          *id = "sessions";
  int            i;
  int            j;
  proc_stat_t   *ps;
  char          *fmt;
  int            njids = 0;
  pid_t         *jids, *hold;
  static int     maxjid = 200;
  register pid_t jobid;

  if (attrib != NULL)
    {
    log_err(-1, id, extra_parm);

    rm_errno = RM_ERR_BADPARAM;

    return(NULL);
    }

  if ((jids = (pid_t *)calloc(maxjid, sizeof(pid_t))) == NULL)
    {
    log_err(errno, id, "no memory");

    rm_errno = RM_ERR_SYSTEM;

    return(NULL);
    }

  /* Search for members of session */

  for (i = 0;i < nproc;i++)
    {
    ps = &proc_array[i];

    if (ps->uid == 0)
      continue;

    if ((jobid = ps->session) == 0)
      continue;

    if (LOGLEVEL >= 6)
      {
      sprintf(log_buffer, "%s[%d]: pid %d sid %d",
              id,
              njids,
              ps->pid,
              jobid);

      log_record(
        PBSEVENT_SYSTEM,
        0,
        id,
        log_buffer);
      }

    for (j = 0;j < njids;j++)
      {
      if (jids[j] == jobid)
        break;
      }

    if (j == njids)
      {
      /* not found */

      if (njids == maxjid)
        {
        /* need more space */

        maxjid += 100;

        hold = (pid_t *)realloc(jids, maxjid);

        if (hold == NULL)
          {
          log_err(errno, id, (char *)"realloc");

          rm_errno = RM_ERR_SYSTEM;

          free(jids);

          return(NULL);
          }

        jids = hold;
        }

      jids[njids++] = jobid; /* add jobid to list */
      }
    }

  if (njids == 0)
    {
    free(jids);

    return(NULL);
    }

  fmt = ret_string;

  for (j = 0;j < njids;j++)
    {
    checkret(&fmt, 100);

    if (j == 0)
      {
      sprintf(fmt, "%d",
              (int)jids[j]);
      }
    else
      {
      sprintf(fmt, " %d",
              (int)jids[j]);
      }

    fmt += strlen(fmt);
    }  /* END for (j) */

  free(jids);

  return(ret_string);
  }  /* END sessions() */





